public class Main {
    // String = clase

    public static void main(String[] args) {

        String mensaje = "    Hola, soy un String";

        System.out.println("Mensaje normal: " + mensaje);

        System.out.println("UpperCase: " + mensaje.toUpperCase().trim());
        System.out.println("Lower Case: " + mensaje.toLowerCase());

        System.out.println("Trimp: " + mensaje.trim());
    }

}
