public class Main {
    public static void main(String[] args) {

        // Operadores Aritmeticos

        int variableX = 50, variableY = 10;
        int resultado;

        // Suma +
        resultado = variableX + variableY;
        System.out.println(resultado);

        // Resta -
        resultado = variableX - variableY;
        System.out.println(resultado);

        // Multiplicacion *
        resultado = variableX * variableY;
        System.out.println(resultado);

        // Divisi√≥n /
        resultado = variableX / variableY;
        System.out.println(resultado);

        variableX++; // Incremento ++1
        System.out.println(variableX);

        variableX--; // Decremento --1
        System.out.println(variableX);

        variableX += 100; // Incrementa en un valor dado
        System.out.println(variableX);

        variableX -= 100; // Decrementa en un valor dado
        System.out.println(variableX);

    }
}
