public class Main {
    public static void main(String[] args) {
        // Operadores relacionales
        // >, <, <=, >=, !=, ==.
        int variable1 = 200;
        int variable2 = 200;
        boolean resultado;

        resultado = variable1 > variable2;
        System.out.println(resultado);

        resultado = variable1 < variable2;
        System.out.println(resultado);

        resultado = variable1 == variable2;
        System.out.println(resultado);

        resultado = variable1 >= variable2;
        System.out.println(resultado);

        resultado = variable1 <= variable2;
        System.out.println(resultado);

        resultado = variable1 != variable2;
        System.out.println(resultado);

    }
}
